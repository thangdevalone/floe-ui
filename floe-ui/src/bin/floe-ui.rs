use clap::{Parser, Subcommand};
use console::{style, Term};
use reqwest::Client;
use std::fs;
use std::path::Path;

const GITHUB_BASE_URL: &str = "https://raw.githubusercontent.com/thangdevalone/floe-ui/main/floe-ui/src";

#[derive(Parser)]
#[command(name = "floe-ui")]
#[command(about = "The official CLI for Floe UI — beautifully styled, token-driven components for Iced.")]
struct Cli {
    #[command(subcommand)]
    command: Commands,

    /// Use local path instead of GitHub (for testing)
    #[arg(long, global = true)]
    local: Option<String>,
}

#[derive(Subcommand)]
enum Commands {
    /// Initialize Floe UI (downloads theme, tokens, and base structure)
    Init,
    /// Add a component to your project
    Add {
        /// Name of the component (e.g., button, card)
        name: String,
    },
}

#[tokio::main]
async fn main() {
    let cli = Cli::parse();
    let term = Term::stdout();

    let result = match cli.command {
        Commands::Init => init(&term, cli.local.as_deref()).await,
        Commands::Add { name } => add(&name, &term, cli.local.as_deref()).await,
    };

    if let Err(err) = result {
        let _ = term.write_line(&format!("{} {}", style("Error:").red().bold(), err));
        std::process::exit(1);
    }
}

async fn init(term: &Term, local_path: Option<&str>) -> Result<(), Box<dyn std::error::Error>> {
    term.write_line(&format!("{} Initializing Floe UI...", style("🚀").cyan().bold()))?;

    // Create directories
    fs::create_dir_all("src/floe_ui/theme")?;
    fs::create_dir_all("src/floe_ui/components")?;

    let client = Client::new();

    // Files to fetch
    let files = vec![
        ("lib.rs", "src/floe_ui/mod.rs"),
        ("theme/mod.rs", "src/floe_ui/theme/mod.rs"),
        ("theme/palette.rs", "src/floe_ui/theme/palette.rs"),
        ("theme/tokens.rs", "src/floe_ui/theme/tokens.rs"),
        ("components/icon.rs", "src/floe_ui/components/icon.rs"),
    ];

    for (remote, local) in files {
        fetch_file(&client, remote, local, local_path).await?;
        term.write_line(&format!("  {} {}", style("✓").green(), local))?;
    }

    // Generate components/mod.rs
    let mod_content = "// This file is auto-generated by Floe CLI.\npub mod icon;\n";
    fs::write("src/floe_ui/components/mod.rs", mod_content)?;
    term.write_line(&format!("  {} src/floe_ui/components/mod.rs", style("✓").green()))?;

    term.write_line(&format!("\n{} Floe UI initialized successfully!", style("✨").green().bold()))?;
    term.write_line("You can now use `floe-ui add <component>` to add components.")?;
    
    Ok(())
}

async fn add(name: &str, term: &Term, local_path: Option<&str>) -> Result<(), Box<dyn std::error::Error>> {
    term.write_line(&format!("{} Adding component '{}'...", style("📥").cyan().bold(), name))?;

    let remote = format!("components/{}.rs", name);
    let local = format!("src/floe_ui/components/{}.rs", name);

    let client = Client::new();
    
    match fetch_file(&client, &remote, &local, local_path).await {
        Ok(_) => {
            term.write_line(&format!("  {} {}", style("✓").green(), local))?;
        }
        Err(e) => {
            return Err(format!("Component '{}' not found or failed to download. ({})", name, e).into());
        }
    }

    // Inject mod declaration into components/mod.rs
    let mod_file = "src/floe_ui/components/mod.rs";
    if Path::new(mod_file).exists() {
        let mut content = fs::read_to_string(mod_file)?;
        let mod_decl = format!("pub mod {};", name);
        if !content.contains(&mod_decl) {
            content.push_str(&format!("\n{}", mod_decl));
            fs::write(mod_file, content)?;
            term.write_line(&format!("  {} Registered in {}", style("✓").green(), mod_file))?;
        }
    } else {
        term.write_line(&format!("  {} Warning: {} not found. Did you run `floe-ui init`?", style("!").yellow(), mod_file))?;
    }

    term.write_line(&format!("\n{} Component '{}' added successfully!", style("✨").green().bold(), name))?;

    Ok(())
}

async fn fetch_file(
    client: &Client,
    remote_path: &str,
    local_path: &str,
    local_override: Option<&str>,
) -> Result<(), Box<dyn std::error::Error>> {
    let content = if let Some(base) = local_override {
        // Local fetch
        let path = Path::new(base).join(remote_path);
        fs::read_to_string(&path)
            .map_err(|e| format!("Failed to read local file {}: {}", path.display(), e))?
    } else {
        // Remote fetch
        let url = format!("{}/{}", GITHUB_BASE_URL, remote_path);
        let resp = client.get(&url).send().await?;
        if !resp.status().is_success() {
            return Err(format!("HTTP {}", resp.status()).into());
        }
        resp.text().await?
    };

    // Rewrite `crate::` imports to `crate::floe_ui::` since files are moving into a sub-module.
    // We only rewrite standard paths used by Floe UI.
    let content = content
        .replace("crate::components", "crate::floe_ui::components")
        .replace("crate::theme", "crate::floe_ui::theme")
        .replace("crate::FloeTheme", "crate::floe_ui::FloeTheme")
        .replace("crate::DesignTokens", "crate::floe_ui::DesignTokens")
        .replace("crate::prelude", "crate::floe_ui::prelude");

    fs::write(local_path, content)?;

    Ok(())
}
